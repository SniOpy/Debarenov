    <h2>Introduction</h2>
    <p>
      DEBARENOV (ci-après "nous", "notre" ou "l'entreprise") s'engage à protéger la confidentialité et la 
      sécurité des données personnelles de ses utilisateurs et clients. La présente politique de confidentialité 
      décrit comment nous collectons, utilisons, stockons et protégeons vos données personnelles conformément 
      au Règlement Général sur la Protection des Données (RGPD) et à la loi Informatique et Libertés.
    </p>
    <p>
      En utilisant notre site web ou nos services, vous acceptez les pratiques décrites dans cette politique 
      de confidentialité.
    </p>

    <h2>Données collectées</h2>
    <p>
      Nous collectons les données personnelles suivantes lorsque vous utilisez notre site ou nos services :
    </p>
    <ul>
      <li><strong>Données d'identification</strong> : nom, prénom, adresse email, numéro de téléphone</li>
      <li><strong>Données de localisation</strong> : adresse postale, code postal, ville</li>
      <li><strong>Données de navigation</strong> : adresse IP, type de navigateur, pages visitées, durée de visite</li>
      <li><strong>Données de commande</strong> : informations relatives à vos demandes de devis et commandes</li>
      <li><strong>Données de communication</strong> : correspondances échangées avec notre service client</li>
    </ul>
    <p>
      Ces données sont collectées directement auprès de vous lorsque vous remplissez un formulaire de contact, 
      demandez un devis, ou indirectement via des cookies et technologies similaires lors de votre navigation.
    </p>

    <h2>Utilisation des données</h2>
    <p>
      Vos données personnelles sont utilisées aux fins suivantes :
    </p>
    <ul>
      <li>Traitement de vos demandes de contact et de devis</li>
      <li>Exécution des prestations commandées</li>
      <li>Gestion de la relation client et du suivi commercial</li>
      <li>Amélioration de nos services et de votre expérience utilisateur</li>
      <li>Envoi d'informations commerciales (avec votre consentement)</li>
      <li>Respect de nos obligations légales et réglementaires</li>
      <li>Analyse statistique et mesure d'audience du site</li>
    </ul>

    <h2>Base légale du traitement</h2>
    <p>
      Le traitement de vos données personnelles est fondé sur :
    </p>
    <ul>
      <li><strong>Votre consentement</strong> : pour l'envoi de communications marketing et l'utilisation de cookies</li>
      <li><strong>L'exécution d'un contrat</strong> : pour le traitement de vos commandes et l'exécution des prestations</li>
      <li><strong>Notre intérêt légitime</strong> : pour l'amélioration de nos services et la prévention de la fraude</li>
      <li><strong>Une obligation légale</strong> : pour la conservation de certaines données comptables et fiscales</li>
    </ul>

    <h2>Conservation des données</h2>
    <p>
      Nous conservons vos données personnelles uniquement pendant la durée nécessaire aux finalités pour lesquelles 
      elles ont été collectées :
    </p>
    <ul>
      <li><strong>Données clients</strong> : durée de la relation commerciale + 3 ans après la fin de la relation</li>
      <li><strong>Données de prospection</strong> : 3 ans à compter du dernier contact</li>
      <li><strong>Données de navigation</strong> : 13 mois maximum</li>
      <li><strong>Données comptables</strong> : 10 ans conformément aux obligations légales</li>
    </ul>
    <p>
      Passé ces délais, vos données sont supprimées ou anonymisées de manière sécurisée.
    </p>

    <h2>Vos droits (RGPD)</h2>
    <p>
      Conformément au RGPD et à la loi Informatique et Libertés, vous disposez des droits suivants concernant 
      vos données personnelles :
    </p>
    <ul>
      <li><strong>Droit d'accès</strong> : vous pouvez obtenir une copie de vos données personnelles</li>
      <li><strong>Droit de rectification</strong> : vous pouvez demander la correction de données inexactes</li>
      <li><strong>Droit à l'effacement</strong> : vous pouvez demander la suppression de vos données dans certains cas</li>
      <li><strong>Droit à la limitation</strong> : vous pouvez demander la limitation du traitement de vos données</li>
      <li><strong>Droit à la portabilité</strong> : vous pouvez récupérer vos données dans un format structuré</li>
      <li><strong>Droit d'opposition</strong> : vous pouvez vous opposer au traitement de vos données pour motifs légitimes</li>
      <li><strong>Droit de retrait du consentement</strong> : vous pouvez retirer votre consentement à tout moment</li>
    </ul>
    <p>
      Pour exercer ces droits, vous pouvez nous contacter à l'adresse : 
      <a href="mailto:contact@debarenov.fr">contact@debarenov.fr</a>
    </p>
    <p>
      Vous disposez également du droit d'introduire une réclamation auprès de la Commission Nationale de 
      l'Informatique et des Libertés (CNIL) si vous estimez que le traitement de vos données personnelles 
      constitue une violation du RGPD.
    </p>

