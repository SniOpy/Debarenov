      et analyser le trafic du site. Les cookies sont de petits fichiers texte stockés sur votre appareil.
    </p>
    <p>
      Nous utilisons les types de cookies suivants :
    </p>
    <ul>
      <li><strong>Cookies strictement nécessaires</strong> : indispensables au fonctionnement du site</li>
      <li><strong>Cookies de performance</strong> : pour analyser l'utilisation du site et améliorer nos services</li>
      <li><strong>Cookies de fonctionnalité</strong> : pour mémoriser vos préférences</li>
      <li><strong>Cookies de ciblage</strong> : pour vous proposer des contenus adaptés (avec votre consentement)</li>
    </ul>
    <p>
      Vous pouvez gérer vos préférences de cookies à tout moment via les paramètres de votre navigateur. 
      Notez que la désactivation de certains cookies peut affecter le fonctionnement du site.
    </p>

    <h2>Sécurité des données</h2>
    <p>
      Nous mettons en œuvre des mesures techniques et organisationnelles appropriées pour protéger vos données 
      personnelles contre tout accès non autorisé, perte, destruction ou altération :
    </p>
    <ul>
      <li>Chiffrement des données sensibles</li>
      <li>Accès restreint aux données personnelles</li>
      <li>Sauvegardes régulières</li>
      <li>Mise à jour régulière de nos systèmes de sécurité</li>
      <li>Formation du personnel à la protection des données</li>
    </ul>
    <p>
      Malgré ces mesures, aucun système n'est totalement sécurisé. Nous ne pouvons garantir une sécurité 
      absolue de vos données.
    </p>

    <h2>Partage des données</h2>
    <p>
      Vos données personnelles ne sont pas vendues, louées ou partagées avec des tiers à des fins commerciales. 
      Nous pouvons partager vos données uniquement dans les cas suivants :
    </p>
    <ul>
      <li>Avec nos prestataires de services (hébergement, outils d'analyse) sous contrat de confidentialité</li>
      <li>Lorsque la loi l'exige ou en réponse à une demande judiciaire</li>
      <li>Pour protéger nos droits, votre sécurité ou celle d'autrui</li>
      <li>Avec votre consentement explicite</li>
    </ul>

